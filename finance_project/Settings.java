    private void saveSettingsToFile(String currency, String theme) {
        JSONObject settings = new JSONObject();
        settings.put("currency", currency);
        settings.put("theme", theme);

        try (FileWriter file = new FileWriter("settings.json")) {
            file.write(settings.toJSONString());
            file.flush();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private void loadSettingsFromFile() {
        File file = new File("settings.json");
        if (file.exists()) {
            try {
                JSONParser parser = new JSONParser();
                JSONObject settings = (JSONObject) parser.parse(new FileReader(file));
                String currency = (String) settings.get("currency");
                String theme = (String) settings.get("theme");

                jComboBox1.setSelectedItem(currency);  // Set the combo box to the saved currency
                jComboBox2.setSelectedItem(theme);  // Set the combo box to the saved theme

                // Apply the saved settings
                applyCurrency(currency);
                applyTheme(theme);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

