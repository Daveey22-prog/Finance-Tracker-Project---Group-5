    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String goalname = jTextField1.getText().trim();
        String targetamount = jTextField2.getText().trim();
        String deadline = jTextField3.getText().trim();

        // Validate input fields
        if (goalname.isEmpty() || targetamount.isEmpty() || deadline.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Input!");
            return;
        }

        String filePath = "src\\finance_project\\data.json";
        JSONParser parser = new JSONParser();

        try (FileReader reader = new FileReader(filePath)) {
            JSONObject jsonObject = (JSONObject) parser.parse(reader);
            JSONArray eventsArray = (JSONArray) jsonObject.get("goals");

            // Check if username already exists
            for (Object obj : eventsArray) {
                JSONObject user = (JSONObject) obj;
                if (user.get("goalname").equals(goalname)) {
                    JOptionPane.showMessageDialog(this, "Error");
                    return;
                }
            }

            // Add new event
            JSONObject newUser = new JSONObject();
            newUser.put("goalname", goalname);
            newUser.put("targetamount", targetamount);
            newUser.put("deadline", deadline);
            eventsArray.add(newUser);

            // Write back to the file
            try (FileWriter file = new FileWriter(filePath)) {
                jsonObject.put("goals", eventsArray);
                file.write(jsonObject.toJSONString());
                file.flush();
            }

            JOptionPane.showMessageDialog(this, "Goals added successfully!");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "File not found or unreadable!", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Error parsing JSON file!", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

