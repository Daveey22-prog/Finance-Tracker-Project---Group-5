    private void loadEventsIntoTable() {
        String filePath = "src\\finance_project\\data.json"; // Path to your JSON file
        JSONParser parser = new JSONParser();

        try {
            // Read the JSON file
            JSONObject jsonObject = (JSONObject) parser.parse(new FileReader(filePath));
            JSONArray eventsArray = (JSONArray) jsonObject.get("goals");

            // Create a DefaultTableModel to hold the data
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Goal Name");
            model.addColumn("Target Amount");
            model.addColumn("Deadline");

            // Iterate through the events and add them to the model
            for (Object obj : eventsArray) {
                JSONObject event = (JSONObject) obj;
                String goalname = (String) event.get("goalname");
                String targetamount = (String) event.get("targetamount");
                String deadline = (String) event.get("deadline");

                // Add a new row to the model
                model.addRow(new Object[]{goalname, targetamount, deadline});
            }

            // Set the model to the jTable
            jTable1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading events: " + e.getMessage());
        }
    }

   

