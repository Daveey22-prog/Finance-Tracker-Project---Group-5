    private void loadSettingsFromFile() {
        File file = new File("settings.json");
        if (file.exists()) {
            try {
                JSONParser parser = new JSONParser();
                JSONObject settings = (JSONObject) parser.parse(new FileReader(file));
                String currency = (String) settings.get("currency");
                String theme = (String) settings.get("theme");

                // Store the loaded settings
                this.currency = currency;
                this.theme = theme;
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

    private void applySettings() {
        // Apply the loaded settings
        applyTheme(theme);
    }


