    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String currentUsername = username.getText().trim();
        String adddate = date.getText().trim();
        String addamount = amount.getText().trim();
        String addtype = type.getSelectedItem().toString().trim();
        String addcategory = category.getSelectedItem().toString().trim();

        // Validate input
        if (currentUsername.isEmpty() || adddate.isEmpty() || addamount.isEmpty() || addtype.isEmpty() || addcategory.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all the fields!");
            return;
        }

        String filePath = "src\\finance_project\\data.json";
        File file = new File(filePath);

        if (!file.exists()) {
            JOptionPane.showMessageDialog(this, "Error! File not found.");
            return;
        }

        JSONParser parser = new JSONParser();
        try (FileReader reader = new FileReader(file)) {
            JSONObject jsonObject = (JSONObject) parser.parse(reader);
            JSONArray addexpense = (JSONArray) jsonObject.get("users");

            boolean found = false;

            // Iterate through users to find the matching username
            for (Object obj : addexpense) {
                JSONObject expense = (JSONObject) obj;
                if (expense.get("username").equals(currentUsername)) {
                    expense.put("date", adddate);
                    expense.put("amount", addamount);
                    expense.put("type", addtype);
                    expense.put("category", addcategory);
                    found = true;
                    break;
                }
            }

            if (!found) {
                JOptionPane.showMessageDialog(this, "Account not found!");
                return;
            }

            // Write updated data back to the JSON file
            try (FileWriter writer = new FileWriter(file)) {
                writer.write(jsonObject.toJSONString());
                writer.flush();
            }

            // Show success message
            JOptionPane.showMessageDialog(this, "Expense added successfully!");

            // Clear the input fields
            username.setText("");
            date.setText("");
            amount.setText("");
            type.setSelectedItem("");
            category.setSelectedItem("");
            String selectedUsername = null;

            // Refresh the Dashboard with updated data
            Dashboard dashboard = new Dashboard(currentUsername); // Pass the username back to Dashboard
            //dashboard.loadJsonToTable();  // Refresh the table
            dashboard.setVisible(true);   // Open the dashboard again
            this.dispose();  // Close AddExpense window

        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading file");
            e.printStackTrace();
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Error parsing file");
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

