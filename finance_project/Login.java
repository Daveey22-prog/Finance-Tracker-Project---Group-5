    public static void filecheck() throws FileNotFoundException, IOException, ParseException {
        FileReader reader = new FileReader(filepath);

        if (reader.ready()) {

            Scanner sc = new Scanner(reader);
            String line = "";

            while (sc.hasNext()) {
                line = line + sc.nextLine();
            }

            if (!line.equals("")) {

                reader.close();
                FileReader reader2 = new FileReader(filepath);
                record = (JSONObject) jsonParser.parse(reader2);
                userlist = (JSONArray) record.get("users");
                reader2.close();
            }

        }

    }

    private void saveRememberMe(String username, String password) {
        try {
            JSONObject rememberData = new JSONObject();
            rememberData.put("username", username);
            rememberData.put("password", password);

            // Ensure the remember.json file is created and written to
            try (FileWriter fileWriter = new FileWriter("remember.json")) {
                fileWriter.write(rememberData.toJSONString());
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private void clearRememberMe() {
        File file = new File("remember.json");
        if (file.exists()) {
            file.delete();  // Delete the file to clear the credentials
        }
    }

