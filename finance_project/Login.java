    private void loadRememberMe() {
        File file = new File("remember.json");
        if (file.exists() && file.length() > 0) {
            try {
                String content = new String(Files.readAllBytes(Paths.get("remember.json")));
                JSONObject rememberData = (JSONObject) jsonParser.parse(content);

                // Only set values if the data is available and valid
                if (rememberData.containsKey("username") && rememberData.containsKey("password")) {
                    jTextField1.setText((String) rememberData.get("username"));
                    jPasswordField1.setText((String) rememberData.get("password"));
                    jCheckBox1.setSelected(true); // Ensure the checkbox is selected
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

