    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        try {
            uname = jTextField1.getText();
            password = new String(jPasswordField1.getPassword());

            if (uname.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter both username and password.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            filecheck(); // Load user data from JSON file

            int checker = 0;
            for (int a = 0; a < userlist.size(); a++) {
                JSONObject userobject = (JSONObject) userlist.get(a);
                String searcheduname = (String) userobject.get("username");
                String searchedpswd = (String) userobject.get("password");

                if (uname.equals(searcheduname) && password.equals(searchedpswd)) {
                    checker++;
                    break;
                }
            }

            if (checker == 0) {
                JOptionPane.showMessageDialog(this, "No account has been found.", "Login Failed", JOptionPane.ERROR_MESSAGE);
            } else {
                //Store the username globally in UserSession
                //currentUsername = uname;

                // Existing code...
                if (checker > 0) {
                    // Pass the username to Dashboard
                    UserSession.currentUsername = uname;
                    Dashboard x = new Dashboard(uname);  // Pass the username
                    x.setVisible(true);
                    this.dispose();  // Close the login window
                }
                // Existing code...
            }

            // Save credentials if "Remember Me" is checked
            if (jCheckBox1.isSelected()) {
                saveRememberMe(uname, password);  // Save credentials
            } else {
                clearRememberMe();  // Clear the credentials if not selected
            }

        } catch (IOException | ParseException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_LoginButtonActionPerformed

